function showPost(postId) {
    const post = blogPosts[postId];
    if (post) {
        document.getElementById('postContent').innerHTML = post.content;
        document.getElementById('blogPostContent').classList.remove('hidden');
        document.body.style.overflow = 'hidden';
    }
}

function hidePost() {
    document.getElementById('blogPostContent').classList.add('hidden');
    document.body.style.overflow = 'auto';
}

// Event listeners
document.addEventListener('DOMContentLoaded', function() {
    // Show post buttons
    document.querySelectorAll('.show-post-button').forEach(button => {
        button.addEventListener('click', function() {
            const postId = this.getAttribute('data-post');
            showPost(postId);
        });
    });

    // Back to blog button
    document.getElementById('backToBlogButton')?.addEventListener('click', hidePost);

    // Close button
    document.getElementById('closePostButton')?.addEventListener('click', hidePost);

    // Close modal when clicking outside content
    document.getElementById('blogPostContent')?.addEventListener('click', function(e) {
        if (e.target === this) {
            hidePost();
        }
    });
});
